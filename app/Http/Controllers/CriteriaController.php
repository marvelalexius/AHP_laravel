    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validate($request, [
            'criteria' => 'required',
        ]);

        $criteria = new Criteria;
        $criteria->name = $request->criteria;
        $criteria->save();
        
        $criteria_relation = new CriteriaRelation;
        $criteria_relation->first_criteria_id = $criteria->id;
        $criteria_relation->second_criteria_id = $criteria->id;
        $criteria_relation->weight = 1;
        $criteria_relation->save();

        return redirect()->route('criterias.index');
    }

